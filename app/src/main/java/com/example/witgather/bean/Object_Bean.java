package com.example.witgather.bean;

public class Object_Bean {
    private String objectName;

    public String getObjectName() {
        return objectName;
    }

    public void setObjectName(String objectName) {
        this.objectName = objectName;
    }

    public String getObjectInPrice() {
        return objectInPrice;
    }

    public void setObjectInPrice(String objectInPrice) {
        this.objectInPrice = objectInPrice;
    }

    public String getObjectOutPrice() {
        return objectOutPrice;
    }

    public void setObjectOutPrice(String objectOutPrice) {
        this.objectOutPrice = objectOutPrice;
    }

    public String getObjectViewer() {
        return objectViewer;
    }

    public void setObjectViewer(String objectViewer) {
        this.objectViewer = objectViewer;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    private String objectInPrice;
    private String objectOutPrice;
    private String objectViewer;
    private String imageUrl;
}
